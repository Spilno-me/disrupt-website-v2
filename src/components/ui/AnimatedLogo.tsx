interface AnimatedPixelProps {
  config: PixelConfig
  isAnimating: boolean
  darkColor?: string
}

function AnimatedPixel({ config, isAnimating, darkColor = '#2D3142' }: AnimatedPixelProps) {
  const controls = useAnimation()
  const fill = config.color === 'red' ? '#F70D1A' : darkColor
  const wasAnimatingRef = useRef(false)
  const timeoutRef = useRef<NodeJS.Timeout | null>(null)

  // Trigger explosion animation sequence
  useEffect(() => {
    if (isAnimating && !wasAnimatingRef.current) {
      const { x, y, rotate = 0, scale = 1, delay } = config.animation

      // Clear any pending timeout
      if (timeoutRef.current) {
        clearTimeout(timeoutRef.current)
      }

      // Start animation (delay is 0 for simultaneous burst)
      timeoutRef.current = setTimeout(async () => {
        // Phase 1: BURST OUT - quick ease-out with rotation
        await controls.start({
          x,
          y,
          rotate,
          scale,
          transition: {
            type: 'tween',
            duration: 0.15,
            ease: [0.33, 1, 0.68, 1], // ease-out-cubic
          },
        })

        // Phase 2: HOLD - brief pause at displaced/rotated position
        await new Promise(resolve => setTimeout(resolve, 50))

        // Phase 3: SPRING BACK - return with overshoot (rotation springs back too)
        await controls.start({
          x: 0,
          y: 0,
          rotate: 0,
          scale: 1,
          transition: {
            type: 'spring',
            stiffness: 300,
            damping: 12,
            mass: 0.8,
          },
        })
      }, delay)
    }
    wasAnimatingRef.current = isAnimating

    // Cleanup timeout on unmount
    return () => {
      if (timeoutRef.current) {
        clearTimeout(timeoutRef.current)
      }
    }
  }, [isAnimating, controls, config.animation])

  const motionProps = {
    initial: { x: 0, y: 0, rotate: 0, scale: 1 },
    animate: controls,
    style: { transformOrigin: 'center' },
  }

  if (config.type === 'rect') {
    return (
      <motion.rect
        {...motionProps}
        x={config.x}
        y={config.y}
        width={config.width}
        height={config.height}
        rx={config.rx}
        fill={fill}
      />
    )
  }

  return <motion.path {...motionProps} d={config.d} fill={fill} />
}

